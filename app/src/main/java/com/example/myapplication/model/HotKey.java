package com.example.myapplication.model;

public class HotKey {
    private String name;
    private int id;

    // 生成 Getter 和 Setter（右键 → Generate → Getter and Setter）
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
